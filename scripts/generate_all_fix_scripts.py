HOOK_RE = re.compile(
    r"<!-- SCRIPTS_HOOK_START\s*track:\s*(?P<track>[^\n]+)\s*-->"
    r"(?P<table>.*?)"
    r"<!-- SCRIPTS_HOOK_END -->",
    re.DOTALL,
)

TABLE_ROW_RE = re.compile(
    r"^\|\s*`(?P<script>[^`]+\.py)`\s*\|"
    r"\s*(?P<files>[^|]+)\s*\|"
    r"\s*(?P<description>[^|]+)\s*\|",
    re.MULTILINE,
)

